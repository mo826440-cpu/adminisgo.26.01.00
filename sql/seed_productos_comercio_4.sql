LEFT JOIN cat_map c ON c.mysql_id = r.id_cat_mysql
LEFT JOIN marca_nombre mn ON mn.mysql_id = r.id_marca_mysql
LEFT JOIN public.marcas m ON m.comercio_id = 4 AND m.nombre = mn.nombre;
